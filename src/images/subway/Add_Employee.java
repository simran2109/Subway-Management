    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        JFileChooser chooser = new JFileChooser();
        chooser.setFileFilter(new FileNameExtensionFilter("JPEG file","jpg","jpeg"));
        chooser.showOpenDialog(this);
        File sourceFile = chooser.getSelectedFile();
        if(sourceFile!=null){
            String sourceFilePath = sourceFile.getAbsolutePath();
            txtFilePath.setText(sourceFilePath);
          
            BufferedImage thumbnail;
            try{
                thumbnail = Thumbnails.of(sourceFile).size(185, 220).asBufferedImage();
                 lblImage.setIcon(new ImageIcon(thumbnail));
                ByteArrayOutputStream baos = new ByteArrayOutputStream();
                ImageIO.write(thumbnail, "jpg", baos);
                byteImage = baos.toByteArray();
                }
            catch (IOException e){
              JOptionPane.showMessageDialog(null, "Insertion error");
            }   
        }
    }//GEN-LAST:event_jButton1ActionPerformed

