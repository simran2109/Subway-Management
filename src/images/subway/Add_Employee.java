    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        //length check for Salary
        if(empSalary.getText().length()<=3){
            JOptionPane.showMessageDialog(null, "Enter valid Salary");
        }
        //Email-validation
        else if(!email.getText().contains("@") && !email.getText().contains(".")){
            JOptionPane.showMessageDialog(null,"Entered Email is not valid!");
        }
        //Contact-validation
        else if(!((contactNo.getText()).length() == 10)){
            JOptionPane.showMessageDialog(null,"Enter valid Contact Number!");
        }
        else{
            try {
                insert();
            } catch (SQLException ex) {
                Logger.getLogger(Add_Employee.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_jButton2ActionPerformed
        void insert() throws SQLException{
            con=Login.con;
            String query =" insert into emp_db(Fname,Lname,Email,Desg,Salary,Contact,DOB) values (?,?,?,?,?,?,?)";
            prep=con.prepareStatement(query);
            prep.setString(1,firstName.getText());
            prep.setString(2,lastName1.getText());
            prep.setString(3,email.getText());
            prep.setString(4,(jComboBox1.getSelectedItem()).toString());
//            prep.setString();
            prep.setString(5,address.getText());
            prep.setString(6,contactNo.getText());
            prep.executeQuery();
        }
